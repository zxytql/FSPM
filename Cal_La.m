function out_La = Cal_La(f,rou,d)
    gama_o_1 = (7.19 * 1e-3 + 6.09 / (f^2 + 0.227) + 4.81 / ((f - 57)^2 + 1.5)) * f^2 * 1e-5;
    gama_w_1 = (0.067 + 2.4 / ((f * 1e-3 - 22.3)^2 + 6.6) + 7.33 / ((f * 1e-3 - 183.5)^2 + 5) + 4.4 / ((f * 1e-3 - 3.38)^2 + 10)) * f^2 * rou * 1e-10;
    out_La = (gama_o_1 + gama_w_1) * d;
end